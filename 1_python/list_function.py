# using del function
l=[10,20,30,'hello']
del(l[1])               # 20 is delete from list
print(l)

# using pop() function
a=[10,30,40,'hello']
print(a.pop(2))         # here which element are deleted are printed in output
print(a)


# using remove() function

b=[1,2,3,4,'hello']
b.remove('hello')        # here we can write which element we want to delet
print(b)


# using clear() function
d=['hii','hello','adarsh','rai']
d.clear()                           # clear puri list ko blanck kr dete hai
